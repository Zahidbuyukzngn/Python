# clear():    Sözlükteki tüm elemanları kaldırır.
# copy():     Sözlüğün bir kopyasını döndürür.
# fromkeys(): Belirtilen anahtarlar ve değerle bir sözlük döndürür.
# get():      Belirtilen anahtarın değerini döndürür.
# items():    Sözlükteki her bir anahtar-değer çifti için bir demet içeren bir liste döndürür.
# keys():     Sözlüğün anahtarlarını içeren bir liste döndürür.
# pop():      Belirtilen anahtarla eşleşen elemanı sözlükten kaldırır ve döndürür.
# popitem():  Sözlüğe en son eklenen anahtar-değer çiftini kaldırır ve döndürür.
# setdefault(): Belirtilen anahtarın değerini döndürür. Anahtar yoksa, belirtilen değerle anahtarı sözlüğe ekler ve döndürür.
# update():   Belirtilen anahtar-değer çiftleriyle sözlüğü günceller.
# values():   Sözlükteki tüm değerleri içeren bir liste döndürür.